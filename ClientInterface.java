    void fileDialog(){
        //Custom button text
        Object[] options = {"Yes, please","No, thanks"};
        int n = JOptionPane.showOptionDialog(this,
                "Incoming File",
                "File Request",
                JOptionPane.YES_NO_OPTION,
                JOptionPane.QUESTION_MESSAGE,
                null,
                options,
                options[1]);
        if(n==0) client.getFileFromServer();
     
    }
    
    void successDialog(String fileName) {
        //Custom button text
        Object[] options = {"Open File","Continue"};
        int n = JOptionPane.showOptionDialog(this,
                "File Received",
                "Open or Continue chatting?",
                JOptionPane.YES_NO_OPTION,
                JOptionPane.QUESTION_MESSAGE,
                null,
                options,
                options[1]);
        if(n==0){
            try {
                Desktop.getDesktop().open(new File(fileName));
            } catch (IOException ex) {
                ex.printStackTrace();
            }
        }
    }
