    private void txtInputMesageFocusGained(java.awt.event.FocusEvent evt) {
           if(txtInputMesage.getText().equals("Enter Message Here")){
               txtInputMesage.setText("");
               txtInputMesage.setForeground(java.awt.Color.BLACK);

           }
    }


    private void sendMessage(){
       String message;
       if(!txtInputMesage.getText().equals("")){
          message = txtInputMesage.getText();
         
          client.sendMessage(message);
         txtInputMesage.setText("");
       }
       
       
    }
    
